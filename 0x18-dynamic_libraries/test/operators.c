#include <stdio.h>

/**
 * add - add two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: their sum
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: their subtraction
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: their multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: their division
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus division betwee two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: their modulo
 */
int mod(int a, int b)
{
	return (a % b);
}
